import 'package:flutter/material.dart';

//verticalSpacer gives us a sizedbox with some height.

SizedBox verticalSpacer(double height) {
  return SizedBox(
    height: height,
  );
}

//horizontalSpacer gives us a sizedbox with some width.
SizedBox horizontalSpacer(double width) {
  return SizedBox(
    width: width,
  );
}
